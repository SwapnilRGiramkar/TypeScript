"use strict";
//Accessing Array Elements 
let colors = ["Red", "Green", "Blue", "Yellow"];
console.log(colors[1]);
console.log(colors[3]);
console.log(colors[0]);
// adding elements to an array 
colors.push("Pink");
colors.push("Black");
console.log(colors);
//Removing last Element from an array
colors.pop();
console.log(colors);
